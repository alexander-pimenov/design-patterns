package com.github.romankh3.designpatterns.command;

public class Light {

    public void on() {
        System.out.println("Light ON");
    }

    public void off() {
        System.out.println("Light OFF");
    }

    public void down() {
        System.out.println("Light DOWN");
    }

    public void up() {
        System.out.println("Light UP");
    }
}
